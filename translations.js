// ひらがな→カタカナ変換
function toKatakana(str) {
  return str.replace(/[\u3041-\u3096]/g, c => String.fromCharCode(c.charCodeAt(0) + 0x60));
}
// カタカナ→ひらがな変換
function toHiragana(str) {
  return str.replace(/[\u30A1-\u30F6]/g, c => String.fromCharCode(c.charCodeAt(0) - 0x60));
}

// 読み仮名マッチ用：ひらがな入力 → 漢字キーを検索してenを返す
function searchByYomi(q) {

  const qHira = toHiragana((q || "").toLowerCase());
  const matched = new Set();

  for (const [kanji, yomi] of Object.entries(ITEM_YOMI)) {

    const yomiHira = toHiragana((yomi || "").toLowerCase());

    let hit = false;

    // 完全一致
    if (yomiHira === qHira) {
      hit = true;
    }

    // 前方一致
    else if (yomiHira.startsWith(qHira)) {
      hit = true;
    }

    // 部分一致（3文字以上のみ）
    else if (
      qHira.length >= 3 &&
      yomiHira.includes(qHira)
    ) {
      hit = true;
    }

    if (hit) {
      for (const [en, ja] of Object.entries(ITEM_TRANSLATIONS)) {

        if (
          kanji.length >= 2
            ? ja.includes(kanji)
            : ja === kanji
        ) {
          matched.add(en.toLowerCase());
        }
      }
    }
  }

  return matched;
}
